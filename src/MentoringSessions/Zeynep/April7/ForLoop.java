package MentoringSessions.Zeynep.April7;

public class ForLoop {
    public static void main(String[] args) {

        /**
         * simple for loop
         *
         * instead of writing same codes again and again, we are using the for loop
         *
         * loop will iterate until condition is false
         *
         * it has  special syntax
         *
         */

        // counting from smaller to bigger number



        // initialize, condition, increment
        for (int i = 0; i < 5 ; i++) {

        }

        // counting from bigger to smaller number
        for (int i = 5; i > 0; i++) {

        }

        // example : sum the numbers from 1 to 5
        int total = 0;
        for (int i = 1; i <=5 ; i++) {
            total += i;  // total = total + i;

        }

        System.out.println(total);

    }
}
