public class ArraysDefinition {
    /*
    Arrays in Java

    1 - An array is a group of like-typed variables that are referred to by a common name.
    2 - Array can contain primitive data types as well as
      objects of a class depending on the definition of array.
    3 - The advantage of the array is storing large number of value with a single name.
    4 - The size of the array must be specified with an integer NOT long and short.
    5 - The size of an array cannot be resized, once it is created the size fixed.
    6 - An array is an ordered list of values.
        The entire array has a single name.
        Each value has a numeric index(starts from 0).

        Declaring Arrays

        int [] scores = new int[10];
   data type + array name     this number refers to the length of array

        float [] prices = new float[500];
        boolean [] flags;
        flags = new boolean[20];
        char [] codes = new char[1750];

        A Sample for String Array Declaration
        String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
        System.out.println(cars[0]);
        Output will be "Volvo", because its index is 0

        Processing Arrays

        When processing the arrays, we use either for loop or for each loop(enhanced loop)
        because all the elements of the array are same, and size is already known.

     */



}
