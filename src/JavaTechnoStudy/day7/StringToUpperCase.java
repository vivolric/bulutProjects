package JavaTechnoStudy.day7;

public class StringToUpperCase {

    /*
    toUpperCase()
    Converts a string to upper case letters
    String
    */

    public static void main(String[] args) {
        String text = "share the task";
        System.out.println("before toUpperCase: " + text);
        System.out.println("after toUpperCase: " + text.toUpperCase());
    }

}
