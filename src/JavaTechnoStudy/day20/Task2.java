package JavaTechnoStudy.day20;

public class Task2 {
//create a method that calculates perimeter of triangle
    // constraints: method name must be "perimeter"  cevre demek

    public static int perimeter(int a, int b, int c) {
        return a + b + c;
    }

    //create a method that calculates perimeter of rectangle
    public static int perimeter(int side1, int side2, int side3, int side4) {
        return side1 + side2 + side3 + side4;
    }

}