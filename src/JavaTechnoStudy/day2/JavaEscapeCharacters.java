package JavaTechnoStudy.day2;

public class JavaEscapeCharacters {

    public static void main(String[] args) {

        // new line --> \n
        System.out.println("Techno\nStudy");

        // tabulation --> \t
        System.out.println("\tHello");

        // quote --> \"
        System.out.println(" \"Hi guys!\" ");

        // backslash --> \\
        System.out.println("\\");
    }
}
