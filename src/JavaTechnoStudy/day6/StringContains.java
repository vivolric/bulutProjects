package JavaTechnoStudy.day6;

public class StringContains {
  /*
     contains()

    Checks whether a string contains a sequence of characters

    boolean
     */


    public static void main(String[] args) {
        String s1 = "Hello";

        System.out.println("is Hello same as llo: " + s1.equals("llo"));
        System.out.println("Does Hello contains llo: " + s1.equals("llo"));
        System.out.println("Does Hello contains eo: " + s1.equals("eo"));


    }


}
